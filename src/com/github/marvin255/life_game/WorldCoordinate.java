package com.github.marvin255.life_game;

public class WorldCoordinate {
    private final int x;
    private final int y;

    public WorldCoordinate(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }
}
